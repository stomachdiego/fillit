#include "libft.h"

int		ft_first_word(char *s)
{
	int	i;

	i = 0;
	if (*s)
	{
		while (ft_is_space(s[i]))
			i++;
		while (!(ft_is_space(s[i])) && s[i])
			write(1, &s[i++], 1);
	}
	write(1, "\n", 1);
	return (0);
}
