#include "libft.h"

int		ft_rev_print(char *s)
{
	int	i;

	i = 0;
	if (*s)
	{
		while (s[i])
			i++;
		while (i)
			write(1, &s[--i], 1);
	}
	write(1, "\n", 1);
	return (0);
}
