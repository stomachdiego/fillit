#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	a;
	int	b;

	b = n % 10;
	n = n / 10;
	if (b < 0 || n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
		b = -b;
	}
	if (n != 0)
	{
		a = 1;
		while (a * 10 <= n)
			a = a * 10;
		while (a != 0)
		{
			ft_putchar_fd(n / a % 10 + '0', fd);
			a = a / 10;
		}
	}
	ft_putchar_fd(b + '0', fd);
}
